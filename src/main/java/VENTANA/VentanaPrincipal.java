private void agregarMesaARestaurante() {
    int filaSeleccionada = jTable2.getSelectedRow();
    if (filaSeleccionada == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Selecciona un restaurante en la tabla.");
        return;
    }

    String capacidadStr = capacidadMesafield.getText();
    if (capacidadStr.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Ingresa la capacidad de la mesa.");
        return;
    }

    try {
        int capacidad = Integer.parseInt(capacidadStr);
        Restaurante restaurante = listaRestaurantes.get(filaSeleccionada);
        int idMesa = restaurante.getMesasDisponibles().size() + 1;

        MODELO.Mesa nuevaMesa = new MODELO.Mesa(idMesa, capacidad);
        restaurante.agregarMesa(nuevaMesa);

        javax.swing.JOptionPane.showMessageDialog(this, "Mesa agregada exitosamente.");

        capacidadMesafield.setText("");
    } catch (NumberFormatException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "La capacidad debe ser un número entero.");
            javax.swing.JOptionPane.showMessageDialog(this, "Mesa agregada exitosamente.");
        capacidadMesafield.setText("");

        // Si el restaurante de la tabla es el mismo que el del combo, actualiza la tabla
        if (comboRestaurantes.getSelectedIndex() == filaSeleccionada) {
            mostrarMesasDeRestauranteSeleccionado();
        }

    }
}
private void actualizarCombosRestaurantes
        () {
    comboRestaurantes.removeAllItems();
    for (Restaurante r : listaRestaurantes) {
        comboRestaurantes.addItem(r.getNombre());
        comboRestaurantesInventario.addItem(r.getNombre());
    }
    
    
    
}

private void mostrarMesasDeRestauranteSeleccionado() {
    int index = comboRestaurantes.getSelectedIndex();
    if (index == -1) return;

    Restaurante restaurante = listaRestaurantes.get(index);
    modeloTablaMesas.setRowCount(0); // limpiar tabla

    for (MODELO.Mesa mesa : restaurante.getMesasDisponibles()) {
        modeloTablaMesas.addRow(new Object[]{
            mesa.getId(),
            mesa.getCapacidad(),
            mesa.isDisponible() ? "Sí" : "No"
        });
    }
}

    private void registrarRestaurante() {
    String nombre = nombrerestfield.getText();
    String direccion = direccionresfield.getText();
    String telefono = telefonoresfield.getText();

    if (nombre.isEmpty() || direccion.isEmpty() || telefono.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Completa todos los campos del restaurante.");
        return;
    }

    Restaurante nuevo = new Restaurante(idRestaurante++, nombre, direccion, telefono);
    listaRestaurantes.add(nuevo);

    modeloTablaRestaurantes.addRow(new Object[]{
        nuevo.getId(),
        nuevo.getNombre(),
        nuevo.getDireccion(),
        nuevo.getTelefono()
    });

    nombrerestfield.setText("");
    direccionresfield.setText("");
    telefonoresfield.setText("");
    actualizarCombosRestaurantes();

}

    private void registrarCliente() {
    String nombre = nombreclifield.getText();
    String email = emailclifield.getText();
    String telefono = telefonoclifield.getText();
    boolean vegetariano = prefclifield.isSelected();

    if (nombre.isEmpty() || email.isEmpty() || telefono.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Completa todos los campos");
        return;
    }

    Cliente nuevo = new Cliente(idCliente++, nombre, email, telefono, vegetariano);
    listaClientes.add(nuevo);

    modeloTabla.addRow(new Object[]{
        nuevo.getId(),
        nuevo.getNombre(),
        nuevo.getEmail(),
        nuevo.getTelefono(),
        nuevo.getPreferencia() ? "Sí" : "No"
    });

    // Limpiar campos
    nombreclifield.setText("");
    emailclifield.setText("");
    telefonoclifield.setText("");
    prefclifield.setSelected(false);
}
