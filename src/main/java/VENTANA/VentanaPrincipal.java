private void cargarInventarioRestaurante() {
    int index = comboRestaurantesInventario.getSelectedIndex();
    if (index == -1) {
        JOptionPane.showMessageDialog(this, "Selecciona un restaurante primero");
        return;
    }
    
    Restaurante restaurante = listaRestaurantes.get(index);
    actualizarTablaPlatos(restaurante);
    actualizarTablaBebidas(restaurante);
    actualizarTablaPostres(restaurante);
}

private void actualizarTablaPlatos(Restaurante restaurante) {
    DefaultTableModel modelo = (DefaultTableModel) tablaPlatos.getModel();
    modelo.setRowCount(0);
    
    for (Plato p : restaurante.getInventarioPlatos().listar()) {
        modelo.addRow(new Object[]{
            p.getId(),
            p.getNombre(),
            p.getDescripcion(),
            p.getPrecio(),
            p.isEstado() ? "Disponible" : "No disponible"
        });
    }
}

private void actualizarTablaBebidas(Restaurante restaurante) {
    DefaultTableModel modelo = (DefaultTableModel) tablaBebidas.getModel();
    modelo.setRowCount(0);
    
    for (Bebidas b : restaurante.getInventarioBebidas().listar()) {
        modelo.addRow(new Object[]{
            b.getId(),
            b.getNombre(),
            b.getDescripcion(),
            b.getPrecio(),
            b.isEstado() ? "Disponible" : "No disponible",
            b.isAlcoholica() ? "Sí" : "No",
            b.getVolumen()
        });
    }
}

private void actualizarTablaPostres(Restaurante restaurante) {
    DefaultTableModel modelo = (DefaultTableModel) tablaPostres.getModel();
    modelo.setRowCount(0);
    
    for (Postres p : restaurante.getInventarioPostres().listar()) {
        modelo.addRow(new Object[]{
            p.getId(),
            p.getNombre(),
            p.getDescripcion(),
            p.getPrecio(),
            p.isEstado() ? "Disponible" : "No disponible",
            p.isAptoCeliacos() ? "Sí" : "No",
            p.getCalorias()
        });
    }
}
private void agregarPlato() {
    int index = comboRestaurantesInventario.getSelectedIndex();
    if (index == -1) {
        JOptionPane.showMessageDialog(this, "Selecciona un restaurante primero");
        return;
    }
    
    try {
        Restaurante restaurante = listaRestaurantes.get(index);
        Plato nuevo = new Plato(
            restaurante.getInventarioPlatos().cantidad() + 1,
            txtNombrePlato.getText(),
            txtDescripcionPlato.getText(),
            Double.parseDouble(txtPrecioPlato.getText()),
            chkActivoPlato.isSelected()
        );
        
        restaurante.agregarPlato(nuevo);
        actualizarTablaPlatos(restaurante);
        limpiarCamposPlato();
        
        JOptionPane.showMessageDialog(this, "Plato agregado exitosamente");
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Ingresa un precio válido");
    }
}

private void limpiarCamposPlato() {
    txtNombrePlato.setText("");
    txtDescripcionPlato.setText("");
    txtPrecioPlato.setText("");
    chkActivoPlato.setSelected(false);
}

